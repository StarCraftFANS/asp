void user_io_put(const char *msg,
		unsigned len)
{
  unsigned i;

  for (i=0; i < len; i++) {
    rs232_send((uint8_t)msg[i]);
  }
}

////////////////////////////////////////////////////////////////

void user_io_put_line(const char *msg,
		      unsigned len)
{
  user_io_put(msg, len);
  user_io_new_line();
}

////////////////////////////////////////////////////////////////

void user_io_new_line(void)
{
  rs232_send(10);
  rs232_send(13);
}

////////////////////////////////////////////////////////////////

int user_io_get(uint8_t *data)
{
  return rs232_recv(data);
}

